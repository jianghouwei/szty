package com.szty.ahnew.model;

public class TrainData {

	private String source;

	private String type;

	private TrainParams params;

	public String getSource() {
		return source;
	}

	public void setSource(String source) {
		this.source = source;
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	public TrainParams getParams() {
		return params;
	}

	public void setParams(TrainParams params) {
		this.params = params;
	}

}
